// Hardware SPI helpers for ST7789 (PB13=SCK, PB15=MOSI = SPI2 on STM32L476)
// CS (PB12), DC (PB11), RST (PB2) remain GPIO-controlled.

static inline void spiWrite(uint8_t data) {
    SPI.transfer(data);
}

static inline void spiWrite16(uint16_t data) {
    SPI.transfer16(data);
}

